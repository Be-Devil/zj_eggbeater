#include "delay.h"
void delay_ms(u16 nms)
{	 	 

	while(nms)
	{
		delay();
		nms--;
	}
	
} 

/*******************************************************************************
* 函 数 名       : delay
* 函数功能		   : 延时函数1ms
*******************************************************************************/
void delay()
{
	u8 i = 111;
	while(i--);	
}
